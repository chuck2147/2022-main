package frc.robot.Record;

public class ShooterSpeeds {
    public ShooterSpeeds(String newName, double newDistance, double newUpperSpeed, double newLowerSpeed) {
        this.Name = newName;
        this.Distance = newDistance;
        this.UpperSpeed = newUpperSpeed;
        this.LowerSpeed = newLowerSpeed;
    }

    public String Name;
    public double Distance;
    public double UpperSpeed;
    public double LowerSpeed;
}
